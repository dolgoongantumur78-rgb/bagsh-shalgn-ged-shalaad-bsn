export function getProfessionBySlug(slug: string): Profession | undefined {
  return PROFESSIONS.find((p) => p.slug === slug);
}

export function getProfessionsByCategory(category: CategoryKey): Profession[] {
  return PROFESSIONS.filter((p) => p.category === category);
}

export function searchProfessions(query: string): Profession[] {
  const q = query.toLowerCase();
  return PROFESSIONS.filter(
    (p) =>
      p.titleMn.toLowerCase().includes(q) ||
      p.titleEn.toLowerCase().includes(q) ||
      p.description.toLowerCase().includes(q) ||
      p.skills.some((s) => s.toLowerCase().includes(q))
  );
}

export function getRecommendedProfessions(
  mbtiType: string,
  iqLevel: RequirementLevel,
  eqLevel: RequirementLevel,
  topN = 5
): Profession[] {
  const mbtiGroup = (t: string) => {
    const groups: Record<string, string[]> = {
      NT: ["INTJ", "INTP", "ENTJ", "ENTP"],
      NF: ["INFJ", "INFP", "ENFJ", "ENFP"],
      SJ: ["ISTJ", "ISFJ", "ESTJ", "ESFJ"],
      SP: ["ISTP", "ISFP", "ESTP", "ESFP"],
    };
    return Object.entries(groups).find(([, v]) => v.includes(t))?.[0] ?? "";
  };

  const userGroup = mbtiGroup(mbtiType);

  return PROFESSIONS.map((p) => {
    let score = 0;
    if (p.mbtiTypes.includes(mbtiType)) score += 3;
    else if (p.mbtiTypes.some((t) => mbtiGroup(t) === userGroup)) score += 1;
    if (p.iqRequirement === iqLevel) score += 2;
    if (p.eqRequirement === eqLevel) score += 2;
    return { p, score };
  })
    .sort((a, b) => b.score - a.score)
    .slice(0, topN)
    .map((x) => x.p);
}
